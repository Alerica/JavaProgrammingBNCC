package bnccsesi0;

public class BNCCSesi0 {

    public static void main(String[] args) {
        int x = 10; // Integer, store a decimal value
        char c = 'a'; // Character, store a single character
        float f = 10.43f; // Float, store a floating number
        double d = 3.14; // Double, store a floating with better precision
        String s = "Hello World";
        
        System.out.println("This is an integer: " + x);
        System.out.println("This is a character: " + c);
        System.out.println("This is a float: " + f);
        System.out.println("This is a double: " + d);
        System.out.println("This is a String: " + s);
    }
    
}
