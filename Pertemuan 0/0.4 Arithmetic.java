package bnccsesi0;

public class BNCCSesi0 {

    public static void main(String[] args) {
        int a = 10;
        int b = 3;
        
        System.out.println("a + b is : " + (a + b));
        System.out.println("a - b is : " + (a - b));
        System.out.println("a * b is : " + (a * b));
        System.out.println("a / b is : " + (a / b));
        System.out.println("a % b is : " + (a % b)); // 10 mod 3 have a remainder of 1, hence the output is 1
    }
    
}

/* Output of the Program
a + b is : 13
a - b is : 7
a * b is : 30
a / b is : 3
a % b is : 1
*/
