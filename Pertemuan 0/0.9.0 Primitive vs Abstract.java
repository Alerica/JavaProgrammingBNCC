package bnccsesi0;

public class BNCCSesi0 {

    public static void main(String[] args) {
        /* Primitive vs Abstract data type
        Abstract data type are object variant of the primitive data type
        Abstract data type are usually start with uppercase
        */
        Integer x; // int
        Double d;  // double
        Boolean b; // boolean
        String s;  // string (there is no primtive version of string)
        
        System.out.println(Integer.parseInt("8932")); 
        // Abstract data type has some built in function that can be helpful, such as, converting String to int
    } 
    
}
/* Program output
8932
*/
